def build_prompt(markdown):
    return f"""
Ты анализируешь сообщения из чата Telegram и строишь структурированную повестку собрания.

ВАЖНО:
- Выводи ТОЛЬКО один корректный JSON объект.
- Без Markdown, без объяснений.
- Используй двойные кавычки.
- Без лишних запятых.
- Язык: русский.

ОСНОВНАЯ ЗАДАЧА:
1. Создай краткое, но информативное резюме обсуждения (3–5 предложений).
2. Извлеки ВСЕ вопросы из чата, точно как они написаны.
3. Присоедини ответы к вопросам, если они есть.
4. Сохраняй авторов вопросов и ответов.

ДОПОЛНИТЕЛЬНО:
- Если вопросов больше 20, выбери 20 наиболее значимых в разделе `agenda`.
- Остальные вопросы помести в `omitted_questions` (только текст и автор).
- Для вопросов без ответа оставляй массив `answers` пустым.
- Не придумывай ответы, не обобщай и не перефразируй вопросы.

СТРУКТУРА JSON:

{{
  "chat_summary": {{
    "text": "3-5 предложений с конкретным описанием обсуждения"
  }},

  "agenda": [
    {{
      "question": "оригинальный текст вопроса",
      "asked_by": "имя автора",
      "answers": [
        {{
          "answer": "оригинальный текст ответа",
          "answered_by": "имя автора"
        }}
      ]
    }}
  ],

  "omitted_questions": [
    {{
      "question": "текст вопроса, который не вошел в 20",
      "asked_by": "имя автора"
    }}
  ]
}}

ПРАВИЛА ФОРМАТИРОВАНИЯ:
- Каждый объект `agenda` = один вопрос.
- `answers` всегда массив (может быть пустым).
- Порядок вопросов сохраняй естественный, как в чате.
- Не ограничивай длину текста вопросов/ответов, но если текст слишком длинный, добавь поле `"truncated": true`.
- `omitted_questions` содержит только текст и автора, без ответов.

VALIDATION:
- JSON должен быть валидным.
- Все строки в двойных кавычках.
- Не придумывай ничего сверх того, что есть в сообщениях.

Сообщения:
{markdown}
"""